<p>This Cookie Policy supplements and clarifies the general Privacy Policy and Personal Data Processing Policy published at: /about. This Policy describes the types of cookies used and their purpose.</p>
<p>Cookies are data sets stored on the device from which the user visited the Website. Their use improves the security and convenience of using the Website, for example by allowing users to:</p>
<ul>
    <li>stay signed in on the Website;</li>
    <li>save settings and preferences;</li>
    <li>use search more conveniently;</li>
    <li>analyze usage statistics.</li>
</ul>
<p>We do not store any personal information in cookies, such as names, email addresses, phone numbers, and similar data.</p>

<h3>Types of cookies used and how to manage them</h3>
<p>Technical cookies allow us to recognize visitors, store selected display settings, and other personal preferences. These cookies are set automatically when visiting the Website.</p>
<p>Analytics cookies ensure the collection of anonymized statistical information related to the functioning of the Website as a whole. We use the Yandex.Metrica web analytics tool for this purpose.</p>
<p>Advertising cookies are used because advertising displayed on the Website is the main source of funding required for its operation and development. Advertising is served by the Yandex Advertising Network, which uses cookies to display ads that may be of interest to you.</p>
<p>You can read more about Yandex service cookies on the page https://yandex.ru/legal/cookies_policy/ru/.</p>
<p>You can manage cookies through your browser settings.</p>
